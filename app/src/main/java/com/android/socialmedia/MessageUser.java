package com.android.socialmedia;

public class MessageUser {
    String id, date;

    public MessageUser() {
    }

    public MessageUser(String id, String date) {
        this.id = id;
        this.date = date;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
